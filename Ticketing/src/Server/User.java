package Server;

class User{
    String name;
    String surname;
    String telephoneNumber;
    String row;
    String col;

    User(String n, String s, String t, String r, String c){
        this.name = n;
        this.surname = s;
        this.telephoneNumber = t;
        this.row = r;
        this.col = c;
    }
}